import { Controller } from 'egg';

class DanmuController extends Controller {
	async query() {
		const { ctx, service } = this;
		const { query } = ctx;

		ctx.helper.validate('query', query);

		const result = await service.danmu.query(query).catch(() => 15000);
		ctx.helper.send(result);
	}

	async info() {
		const { ctx, service } = this;
		const id = ctx.params.id;

		ctx.helper.validate('id', { id });

		const result = await service.danmu.info(id).catch(() => 15001);
		ctx.helper.send(result);
	}

	async create() {
		const { ctx, service } = this;
		const data = ctx.request.body;
		const userId = ctx.state.user.name;

		data.author = userId;
		ctx.helper.validate('danmu', data, true);

		const result = await service.danmu.create(data).catch(() => 15002);
		ctx.helper.send(result);
	}

	async update() {
		const { ctx, service } = this;
		const data = ctx.request.body;
		const id = ctx.params.id;

		ctx.helper.validate('id', { id });
		ctx.helper.validate('danmu', data);

		const result = await service.danmu.update([ id ], data).catch(() => 15003);
		ctx.helper.send(result);
	}

	async updateMany() {
		const { ctx, service } = this;
		const data = ctx.request.body;
		const { ids } = data;

		ctx.helper.validate('ids', { ids });
		ctx.helper.validate('danmu', data);

		const result = await service.danmu.update(ids, data).catch(() => 15003);
		ctx.helper.send(result);
	}

	async destroy() {
		const { ctx, service } = this;
		const id = ctx.params.id;

		ctx.helper.validate('id', { id });

		const result = await service.danmu.destroy([ id ]).catch(() => 15004);
		ctx.helper.send(result);
	}

	async destroyMany() {
		const { ctx, service } = this;
		const { ids } = ctx.request.body;

		ctx.helper.validate('ids', { ids });

		const result = await service.danmu.destroy(ids).catch(() => 15004);
		ctx.helper.send(result);
	}
}

export default DanmuController;
